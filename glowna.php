	<h5 style="text-align:center;font-size:26px">Rzeszów - stolica Podkarpacia</h5>
	<p style="text-align:justify;padding-left:300px;padding-right:300px"><strong>Rzeszów</strong>, miasto będące stolicą utworzonego w 1999 roku województwa podkarpackiego, leżące w południowo – wschodniej Polsce. Stanowi miasto na prawach powiatu, a według najnowszych danych statystycznych, liczy około 180 tysięcy mieszkańców. <strong>Rzeszów</strong> znajduje się na granicy <strong>Pogórza Karpackiego i Kotliny Sandomierskiej</strong>, nad liczącą 220 kilometrów długości <strong>rzeką Wisłok</strong>.</p>
    <p style="text-align:justify;padding-left:300px;padding-right:300px">Historię osad na terenie dzisiejszego <strong>Rzeszowa</strong> potwierdzają wykopaliska archeologiczne dokumentujące odkrycia sprzed 6 tysięcy lat. Między XI a XII wiekiem, z inicjatywy prawdopodobnie Polaków, powstał gród miejski. Prawa miejskie z kolei nadane zostały 19 stycznia 1354 roku, kiedy to Kazimierz III Wielki przekazał miasto Janowi Pakosławicowi ze Stróżysk. Mimo dewastacji w XV wieku, <strong>Rzeszów</strong> został szybko odbudowany, a na mocy uzyskanych przywilejów zdobył możliwość szybkiego rozwoju. Już wtedy w mieście rozwinęło się m.in. kowalstwo, krawiectwo czy kuśnierstwo. Po bitwie pod Rzeszowem, 13 sierpnia 1769 roku, miasto przez 146 lat wchodziło w skład Imperium Habsburgów. Na powrót wolnym stało się w roku 1845. W czasie II wojny światowej, w Rzeszowie istniało getto żydowskie, które zamieszkiwało około 30% ówczesnych mieszkańców miasta. Większość ze znajdujących się w nim Żydów wywieziono do obozu lub rozstrzelano.</p>
	<p style="text-align:justify;padding-left:300px;padding-right:300px">Obecnie <strong>Rzeszów</strong> stopniowo powiększa swój obszar i dynamicznie się rozwija.</p>
